#!/usr/bin/env python3
"""
Module that provides a type-annotated function to convert a float to a string.
"""


def to_str(n: float) -> str:
    """
    Returns the string representation of a floating-point number.

    Args:
        n (float): The floating-point number to convert.

    Returns:
        str: The string representation of n.
    """
    return str(n)
